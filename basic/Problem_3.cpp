// C++ Program to Swap Values of Two Variables

#include <iostream>
using namespace std;

main (){
    int num1;
    int num2;
    //Logic Here  

    temp = num1;
    num1 = num2;
    num2 = temp;

    //here the valus will be updated
    cout << num1<< endl;
    cout << num2<< endl;

    return 0;


}